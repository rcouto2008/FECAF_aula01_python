# Crie um programa que solicite o nome, 
# a idade e a cidade onde a pessoa mora, 
# e retorne uma frase utilizando essas 
# três informações.

nome = input("Digite seu nome: ")
idade = input("Digite sua idade: ")
cidade = input("Digite a cidade onde você mora: ")

frase = f"Olá {nome}! Você tem {idade} anos e mora em {cidade}."

print (frase)
